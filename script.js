function downloadResults(format) {
let content = '', mimeType = '', extension = '';
if (format === 'txt') { content = state.lastResults.map((set, i) => `Set ${i + 1}: ${set.join(', ')}`).join('\n'); mimeType = 'text/plain'; extension = 'txt'; }
else { content = 'Set,Numbers\n' + state.lastResults.map((set, i) => `${i + 1},"${set.join(',')}"`).join('\n'); mimeType = 'text/csv'; extension = 'csv'; }
const blob = new Blob([content], { type: mimeType });
const url = URL.createObjectURL(blob);
const a = document.createElement('a');
a.href = url; a.download = `keno-${Date.now()}.${extension}`;
document.body.appendChild(a); a.click(); document.body.removeChild(a); URL.revokeObjectURL(url);
showToast(getTranslation('downloaded', { ext: extension }), 'success');
}

