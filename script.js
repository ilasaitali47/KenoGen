function handleGenerate() {
const config = getConfig();
if (!config) return;
if (config.min > config.max) { showToast(getTranslation('errorMinMax'), 'error'); return; }
if (config.include.length > config.count) { showToast(getTranslation('errorInclude', { count: config.include.length, total: config.count }), 'error'); return; }

if (elements.generateBtn) {
elements.generateBtn.classList.add('generating');
elements.generateBtn.innerHTML = `<svg class="animate-spin" width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><circle cx="12" cy="12" r="10" stroke-dasharray="32" stroke-dashoffset="12" stroke-linecap="round"></circle></svg> <span>${getTranslation('generateBtn')}</span>`;
}

setTimeout(() => {
try {
state.lastResults = [];
for (let i = 0; i < state.setsCount; i++) state.lastResults.push(generateSet(config));
renderResults(state.lastResults);
addToHistory(state.lastResults);
if (elements.resultActions) elements.resultActions.style.display = 'flex';
showToast(getTranslation('generated', { count: state.setsCount }), 'success');
} catch (error) {
showToast(getTranslation('errorFilters'), 'error');
} finally {
if (elements.generateBtn) {
elements.generateBtn.classList.remove('generating');
elements.generateBtn.innerHTML = `<svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><polygon points="13 2 3 14 12 14 11 22 21 10 12 10 13 2"></polygon></svg> <span>${getTranslation('generateBtn')}</span>`;
}
}
}, 400);
}

function getConfig() {
const parseNumbers = (str) => str.split(',').map(n => parseInt(n.trim(), 10)).filter(n => !isNaN(n) && n >= 1 && n <= 80);
return {
count: parseInt(elements.numberCount.value, 10),
mode: elements.generationMode.value,
min: parseInt(elements.minValue.value, 10) || 1,
max: parseInt(elements.maxValue.value, 10) || 80,
include: parseNumbers(elements.includeNumbers.value),
exclude: parseNumbers(elements.excludeNumbers.value),
range: elements.rangeFilter.value
};
}

function generateSet(config) {
let attempts = 0;
while (attempts < 100) {
attempts++;
let candidate = [...config.include];
let pool = [];
for (let i = config.min; i <= config.max; i++) {
if (!config.exclude.includes(i) && !candidate.includes(i)) pool.push(i);
}
pool = shuffleArray([...pool]);
const needed = config.count - candidate.length;

if (needed > 0) {
if (config.mode === 'random') candidate = candidate.concat(pool.slice(0, needed));
else if (config.mode === 'clustered') candidate = candidate.concat(generateClustered(pool, needed, config.min, config.max));
else if (config.mode === 'consecutive') candidate = candidate.concat(generateConsecutive(pool, needed, config.min, config.max));
else if (config.mode === 'separated') candidate = candidate.concat(generateSeparated(pool, needed));
else if (config.mode === 'balanced') candidate = candidate.concat(generateBalanced(pool, needed, config.min, config.max));
else if (config.mode === 'hotcold') candidate = candidate.concat(generateHotCold(pool, needed));
}

candidate = [...new Set(candidate)].sort((a, b) => a - b);
if (candidate.length < config.count) continue;
if (passesFilters(candidate, config)) return candidate.slice(0, config.count);
}
throw new Error("Filters");
}

function generateClustered(pool, needed, min, max) {
const result = [];
let remaining = needed;
let currentIdx = Math.floor(Math.random() * pool.length);
while (remaining > 0 && pool.length > 0) {
const clusterSize = Math.min(remaining, Math.floor(Math.random() * 3) + 2);
for (let i = 0; i < clusterSize && remaining > 0; i++) {
if (currentIdx < pool.length) { result.push(pool[currentIdx]); remaining--; }
currentIdx++;
}
currentIdx += Math.floor(Math.random() * 5) + 3;
if (currentIdx >= pool.length) currentIdx = 0;
}
return result;
}

function generateConsecutive(pool, needed, min, max) {
const result = [];
const maxStart = max - needed + 1;
const start = Math.floor(Math.random() * (maxStart - min + 1)) + min;
for (let i = 0; i < needed; i++) result.push(start + i);
return result;
}

function generateSeparated(pool, needed) {
const result = [];
const step = Math.max(1, Math.floor(pool.length / needed));
for (let i = 0; i < needed; i++) result.push(pool[Math.min(i * step, pool.length - 1)]);
return result;
}

function generateBalanced(pool, needed, min, max) {
const result = [];
const mid = Math.floor((min + max) / 2);
const pools = [
pool.filter(n => n <= mid && n % 2 !== 0),
pool.filter(n => n <= mid && n % 2 === 0),
pool.filter(n => n > mid && n % 2 !== 0),
pool.filter(n => n > mid && n % 2 === 0)
];
for (let i = 0; i < needed; i++) {
const poolIdx = i % 4;
if (pools[poolIdx].length > 0) {
result.push(pools[poolIdx].splice(Math.floor(Math.random() * pools[poolIdx].length), 1)[0]);
} else {
const flat = pools.flat();
if (flat.length > 0) result.push(flat.splice(Math.floor(Math.random() * flat.length), 1)[0]);
}
}
return result;
}

function generateHotCold(pool, needed) {
const result = [];
const hotNumbers = [1, 2, 4, 23, 27, 34, 35, 40, 50, 65, 72].filter(n => pool.includes(n));
const weightedPool = [];
pool.forEach(n => {
weightedPool.push(n);
if (hotNumbers.includes(n)) weightedPool.push(n, n, n);
});
for (let i = 0; i < needed; i++) {
if (weightedPool.length === 0) break;
const idx = Math.floor(Math.random() * weightedPool.length);
const num = weightedPool[idx];
result.push(num);
for (let j = weightedPool.length - 1; j >= 0; j--) if (weightedPool[j] === num) weightedPool.splice(j, 1);
}
return result;
}

function passesFilters(numbers, config) {
const mid = 40;
if (config.range === 'low') return numbers.every(n => n <= mid);
if (config.range === 'high') return numbers.every(n => n > mid);
if (config.range === 'mostlyLow') return numbers.filter(n => n <= mid).length >= Math.ceil(numbers.length * 0.6);
if (config.range === 'mostlyHigh') return numbers.filter(n => n > mid).length >= Math.ceil(numbers.length * 0.6);
return true;
}

