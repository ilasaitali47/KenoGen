const elements = {};

function cacheElements() {
const ids = ['languageSelect', 'themeToggle', 'numberCount', 'numberCountDisplay', 'setsCount', 'setsCountDisplay',
'generationMode', 'modeDescription', 'toggleFilters', 'filtersContent', 'includeNumbers', 'excludeNumbers',
'minValue', 'maxValue', 'rangeFilter', 'generateBtn', 'resultsContainer', 'resultActions',
'historyList', 'favoritesList', 'clearHistoryBtn', 'clearFavoritesBtn', 'copyAllBtn',
'downloadTxtBtn', 'downloadCsvBtn', 'toastContainer'];
ids.forEach(id => { elements[id] = document.getElementById(id); });
}

document.addEventListener('DOMContentLoaded', () => {
cacheElements();
loadState();
applyTheme();
setLanguage(state.lang);
setupEventListeners();
updateModeDescription();
renderHistory();
renderFavorites();
});

function loadState() {
const savedHistory = localStorage.getItem('keno-history');
const savedFavorites = localStorage.getItem('keno-favorites');

if (savedHistory) {
let parsed = JSON.parse(savedHistory);
// Migrate old grouped history format to new flat format (one item per set)
if (parsed.length > 0 && parsed[0].sets) {
const migrated = [];
parsed.forEach(group => {
group.sets.forEach(setStr => {
migrated.push({
id: Date.now().toString() + Math.random().toString(36).substr(2, 5),
timestamp: group.timestamp,
numbers: setStr.split(', ').map(Number)
});
});
});
state.history = migrated.slice(0, 50); // Keep max 50
saveState(); // Save the migrated flat structure
} else {
state.history = parsed;
}
}

if (savedFavorites) state.favorites = JSON.parse(savedFavorites);
}

function saveState() {
localStorage.setItem('keno-history', JSON.stringify(state.history));
localStorage.setItem('keno-favorites', JSON.stringify(state.favorites));
localStorage.setItem('keno-theme', state.theme);
localStorage.setItem('keno-lang', state.lang);
}

function setLanguage(lang) {
state.lang = lang;
saveState();
const t = translations[lang];
document.documentElement.lang = lang;
document.documentElement.dir = (lang === 'ar') ? 'rtl' : 'ltr';
document.querySelectorAll('[data-i18n]').forEach(el => {
const key = el.getAttribute('data-i18n');
if (t[key]) el.textContent = t[key];
});
updateModeDescription();
renderHistory();
renderFavorites();
}

function getTranslation(key, vars = {}) {
let t = translations[state.lang].toasts[key] || key;
for (const [k, v] of Object.entries(vars)) t = t.replace(`{${k}}`, v);
return t;
}

